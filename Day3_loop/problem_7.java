package Day3_loop;

import java.util.Scanner;

public class problem_7 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter number: ");
        int n = sc.nextInt();
        int fact = 1;
        for (int i = 1; i <= n; i++) {
            fact = fact * i;
        }
        System.out.printf("Factorial of %d is %d", n, fact);
    }
    
}
